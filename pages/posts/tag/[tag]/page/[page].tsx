import Pagination from "@/components/Pagination/Pagination";
import SinglePost from "@/components/Post/SinglePost";
import {
  getNumberOfPages,
  getPostsByPage,
  getPostsByTagAndPage,
} from "@/lib/notionAPI";
import { GetStaticPaths, GetStaticProps } from "next";
import Head from "next/head";

export const getStaticPaths: GetStaticPaths = async () => {
  return {
    paths: [
      {
        params: { tag: "blog", page: "1" },
      },
    ],
    fallback: "blocking",
  };
};

// SSG:ビルド時にあらかじめデータを取得しておく
export const getStaticProps: GetStaticProps = async (context) => {
  // ?オプショナルチェーン：オプショナルチェーン ?. を使うと、左側がnullishの場合にもエラーにならず、undefinedが返るため、短い書き方でプロパティにアクセスすることができます。
  const currentPage: string = context.params?.page.toString();
  const currentTag: string = context.params?.tag.toString();

  const posts = await getPostsByTagAndPage(
    currentTag,
    parseInt(currentPage, 10)
  );

  return {
    props: {
      posts,
    },
    // ISR:60秒ごとにデータを更新する
    revalidate: 60,
  };
};

const BlogTagPageList = ({ posts, numberOfPage }) => {
  return (
    <div className="container h-full w-full mx-auto">
      <Head>
        <title>Create Next App</title>
        <meta name="description" content="Generated by create next app" />
        <meta name="viewport" content="width=device-width, initial-scale=1" />
        <link rel="icon" href="/favicon.ico" />
      </Head>
      <main className="container w-full mt-16">
        <h1 className="text-5xl font-medium text-center mb-16">
          NotionBlog 🚀
        </h1>
        <section className="sm:grid grid-cols-2 w-5/6 gap-3 mx-auto">
          {posts.map((post) => {
            return (
              <div key={post.id}>
                <SinglePost
                  title={post.title}
                  description={post.description}
                  date={post.date}
                  tags={post.tags}
                  slug={post.slug}
                  isPaginationPage={true}
                />
              </div>
            );
          })}
        </section>
        <Pagination numberOfPage={numberOfPage} />
      </main>
    </div>
  );
};

export default BlogTagPageList;
